  def get_user_by_email(email) do
    from(u in User, join: c in assoc(u, :credential), where: c.email == ^email)
    |> Repo.one()
    |> Repo.preload(:credential)
  end

  def authenticate_by_email_and_pass(email, given_pass) do
    user = get_user_by_email(email)

    cond do
      user && Comeonin.Argon2.checkpw(given_pass, user.credential.password_hash) ->
      {:ok, user}

      user -> { :error, :unauthorized}

      true ->
        Comeonin.Argon2.dummy_checkpw()
        {:error, :unauthorized} # don't show different return because it allows hackers to test password and email seperate
    end
  end

  def set_token(user) do
    user = Repo.preload(user, :credential)
    if user.credential.token == nil do
      token = random_string(64)
      update_token(user, token)
      token
    else
      user.credential.token
    end
  end

  defp update_token(user, token) do
    attrs = %{token: token}
    user.credential
    |> Credential.tokenchangeset(attrs)
    |> Repo.update()
  end

  def remove_token(user) do
    user = Repo.preload(user, :credential)
    update_token(user, nil)
  end

  def random_string(length) do
    :crypto.strong_rand_bytes(length) |> Base.url_encode64 |> binary_part(0, length)
  end

  def check_token(user, token) do
    user = Repo.preload(user, :credential)
    user.credential.token == token
  end

